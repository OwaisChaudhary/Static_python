# Arithmetic operator
a=4
b=30
print("addition: ",a+b)
print("subtraction: ",a-b)
print("multiplication: ",a*b)
print("division: ",a/b)
print("floor division: ",a//b)
print("exponent: ",a**b)
print("mod: ",a%b)

