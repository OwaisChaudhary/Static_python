# Comparision operators
a=10
print(">=",a>=b)
print("<=",a<=b)
print(">",a>b)
print("<",a<b)
print("==",a==b)
print("!=",a!=b)


# Logical operators
a=True
b=False
print("and",a and b)
print("or",a or b)
print("not",not b)

# Bitwise operators
print("bitwise &",a&b)
print("bitwise |",a|b)
print("bitwise ^",a^b)
print("bitwise >>",a>>b)
print("bitwise <<",a<<b)

# Identity operator
print("is",a is b)
print("is not",a is not b)

# Membership operators
a="Hello"
print('l' in a)
print("n" not in a)